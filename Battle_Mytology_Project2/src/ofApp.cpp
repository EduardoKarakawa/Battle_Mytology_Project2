#pragma once
#include "ofApp.h"

#include "jogador.h"
#include "mapa.h"

Jogador player;
Mapa mundo;
ofVec2f v;

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void ofApp::setup(){
	//Iniciando contagem de tempo de execução
	before = ofGetElapsedTimef();

	//Iniciando o Mundo
	mundo.iniciar(0,0);

	//Inicializando o Player
	player.iniciar();

	
	v.set(278, 197);
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void ofApp::update(){
	//Atualizando a contagem de tempo de execução do jogo.
		gameTime = ofGetElapsedTimef() - before;
		before = ofGetElapsedTimef();

		
		player.colidiuCom(v, mundo.posicao, mundo.velocidade, 150);

	//Controlando o player
		
		/*player.mover(teclado, gameTime);
		player.atrito(teclado, gameTime);*/
		player.animacao(gameTime);
		player.acoes(teclado);

	//Controlando o Mundo
		mundo.mover(teclado);
}


//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void ofApp::draw(){
	
	mundo.desenhar();
	std::cout << "x " << mundo.posicao.x << " y " << mundo.posicao.y << "     " << mundo.posicao + v << std::endl;
	ofDrawCircle(mundo.posicao + v, 150);
	ofSetColor(255, 0, 0);
	ofDrawLine(mundo.posicao + v, mundo.posicao + v + 150);
	ofSetColor(255, 255, 255);
	player.desenhar(mundo.posicao);
	

}


