void ofApp::keyPressed(int key){
	//Inputs de movimentação
	if ((key == 'W') || (key == 'w'))
		teclado.keyW = true;
	if ((key == 'S') || (key == 's'))
		teclado.keyS = true;
	if ((key == 'A') || (key == 'a'))
		teclado.keyA = true;
	if ((key == 'D') || (key == 'd'))
		teclado.keyD = true;

	//Inputs de ataque
	if (key == OF_KEY_UP)
		teclado.keyUp = true;
	if (key == OF_KEY_DOWN)
		teclado.keyDown = true;
	if (key == OF_KEY_LEFT	)
		teclado.keyLeft= true;
	if (key == OF_KEY_RIGHT)
		teclado.keyRight = true;
}


//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
