void ofApp::draw(){

	personagem.sprite.drawSubsection(personagem.posicao.x, personagem.posicao.y, personagem.sprite.getWidth(), personagem.sprite.getWidth(), 0, personagem.sprite.getWidth()*personagem.frame);
}


